void StartGPSTask(void *argument)
{
    char local_copy[BUFFER_SIZE];
    HAL_UART_Transmit(&huart3, "Hello world\n", 13, 100);

    for(;;)
    {
        // Wait for semaphore released by IRQ handler
        if (xSemaphoreTake(gpsSemaphore, portMAX_DELAY) == pdTRUE)
        {
            // Determine which buffer finished
            if (hgps.active_rx_buffer == 2) {
                // buffer_1 completed
                strcpy(local_copy, (char*)hgps.rx_buffer_1);
                hgps.rx_buffer_1_pos = 0;
            }
            else {
                // buffer_2 completed
                strcpy(local_copy, (char*)hgps.rx_buffer_2);
                hgps.rx_buffer_2_pos = 0;
            }

            HAL_UART_Transmit(&huart3, (uint8_t*)local_copy, strlen(local_copy), 100);

            enum minmea_sentence_id id = minmea_sentence_id(local_copy, false);
            ParseStatus status;

            if(id == MINMEA_SENTENCE_GGA) {
            	status = parse_gps_sentence(local_copy, &gps_values);
            }

            // For testing: print parsed result
            char msg[128];
            snprintf(msg, sizeof(msg),
                                 "LAT: %.6f  LON: %.6f  ALT: %.2f  Sats: %d\r\n",
                                 gps_values.latitude,
                                 gps_values.longitude,
                                 gps_values.altitude,
                                 gps_values.sats_tracked);

            // HAL_UART_Transmit(&huart3, (uint8_t*)msg, strlen(msg), 100);
        }
    }
}

