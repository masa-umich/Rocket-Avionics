/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* init code for LWIP */
  MX_LWIP_Init();
  /* USER CODE BEGIN 5 */

  telemetrySendTaskHandle = osThreadNew(TelemetrySend, NULL, &telemetry_send_task_attr);

  logListenTaskHandle = osThreadNew(LogListener, NULL, &log_listen_task_attr);

  // Start packet handler
  packetTaskHandle = osThreadNew(ProcessPackets, NULL, &packet_task_attr);

  // Start TCP client
  switch(client_init(&loaded_config.flightcomputerIP, netif_is_link_up(&gnetif))) {
  	case 2: {
  		// threads didn't start
  		log_message(FR_ERR_TCP_CLIENT_INIT_THREAD, -1);
  		break;
  	}
  	case 1: {
  		// memory error
  		log_message(FR_ERR_TCP_CLIENT_INIT_MEM_ERR, -1);
  		break;
  	}
  	default: {
  		// client initialized
  		log_message(FR_STAT_TCP_CLIENT_INIT, -1);
  		break;
  	}
  }

  log_message(STAT_STARTUP_DONE, -1);

  uint32_t startTick = HAL_GetTick();
  uint32_t heartTick = HAL_GetTick();
  uint8_t logdelay = 0;
  for(;;) {
	//size_t freemem = xPortGetFreeHeapSize();
	if(logdelay > 4) {
		handle_logging(); // Log to UDP and flash
	}

	if(HAL_GetTick() - startTick > 250) {
		startTick = HAL_GetTick();
		if(logdelay < 5) {
			logdelay++;
		}
		//size_t freemem = xPortGetFreeHeapSize();
		refresh_log_timers();
	}

	// Send heartbeat
	if(HAL_GetTick() - heartTick > 1000) {
		heartTick = HAL_GetTick();
		if(is_client_connected() > 0) {
			Message heartbeat = {0};
			heartbeat.type = MSG_HEARTBEAT;
			send_msg_to_device(&heartbeat, 5);
		}
	}
	osDelay(5);
  }
  /* USER CODE END 5 */
}

