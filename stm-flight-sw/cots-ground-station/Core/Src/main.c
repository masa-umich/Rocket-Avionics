void irq_uart_lilygo() {
	if(HAL_GetTick() - last_uart_byte > 100) {
		uart_packet_size = 0;
	}
	BaseType_t xHigherPriorityTaskWoken = pdFALSE;
	uart_packet[uart_packet_size++] = next_uart_byte;
	if(uart_packet_size == UART_PACKET_LENGTH) {
		uart_packet_size = 0;
		xSemaphoreGiveFromISR(uart_semaphore, &xHigherPriorityTaskWoken);
	}
	last_uart_byte = HAL_GetTick();
	HAL_UART_Receive_IT(&huart2, &next_uart_byte, 1);
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if(huart->Instance == USART2) {
    	irq_uart_lilygo();
    }
}

void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart) {
    if(huart->Instance == USART2) {
    	last_uart_byte = HAL_GetTick();
    	HAL_UART_Receive_IT(&huart2, &next_uart_byte, 1);
    }
}
