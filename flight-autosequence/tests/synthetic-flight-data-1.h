/**
 * synthetic-flight-data-1.h
 *
 * SCENARIO: "Dual Barometer Failure During Critical Descent"
 *
 * Normal high-altitude supersonic flight (~18km apogee), but:
 *
 * EDGE CASES:
 * 1. IMU1 has a +5 m/s^2 calibration bias on ALL readings (systematic error).
 *    This shifts the fused average upward, delaying MECO detection since
 *    the buffer needs 80% < 0 but the average is biased by +2.5 m/s^2.
 *
 * 2. BOTH barometers fail simultaneously at sample 140 (read 0 hPa) during
 *    the rapid descent phase — exactly when drogue deployment detection matters.
 *    - Bar1 recovers at sample 156 with noisy/unstable readings (±20 hPa jitter)
 *    - Bar2 stays dead until sample 166
 *    This tests dual-sensor imputation: with both sensors failed, mean(buf)
 *    is the only fallback, and recovery must not cause false triggers.
 *
 * 3. Bar1 has a brief spike to 500 hPa at sample 95 (during coast to apogee).
 *    Tests single-sensor imputation when bar2 is still healthy.
 *
 * EXPECTED BEHAVIOR:
 * - MECO detection is delayed ~5-10 samples vs. original due to IMU bias
 * - Drogue/main detection relies on Fluctus or fallback timers during baro blackout
 * - Landing detection may be delayed due to corrupted descent data
 */

