/**
 * synthetic-flight-data-2.h
 *
 * SCENARIO: "Low-Altitude Subsonic Flight with Ambiguous MECO"
 *
 * A short, low-thrust flight reaching only ~800m apogee. The rocket
 * never goes supersonic, so Mach lockout behavior is tested under
 * conditions where it arguably shouldn't activate.
 *
 * EDGE CASES:
 * 1. Ambiguous MECO: acceleration hovers near zero for 10+ samples
 *    (samples 30-42) before going clearly negative. The engine doesn't
 *    cut off cleanly — thrust tapers off over ~600ms. This tests whether
 *    the 80% consensus threshold handles a gradual MECO transition.
 *
 * 2. IMU2 complete failure at sample 50: reads 200 m/s^2 (garbage,
 *    outside ACCEL_MAX=100). fix_reading should impute with buffer mean.
 *    From this point, detection relies on IMU1 alone for the fused average.
 *
 * 3. Persistently noisy Bar1: +-8 hPa random jitter on every reading.
 *    Bar2 is clean. Tests whether sensor fusion smooths out noise
 *    enough for reliable altitude-based detection.
 *
 * 4. Apogee at ~800m — below DROGUE_DEPLOY_ALTITUDE (1524m).
 *    The rocket never climbs above drogue altitude, so pressure is
 *    always above DROGUE_DEPLOY_PRESSURE. Drogue detection should
 *    trigger immediately once the buffer fills in ST_WAIT_DROGUE.
 *
 * 5. Late valve opening: valves open at sample 10 instead of 15.
 *    Less ground calibration data available for T_GROUND / P_GROUND.
 *
 * EXPECTED BEHAVIOR:
 * - MECO detection delayed by ambiguous acceleration signal
 * - IMU2 failure mid-flight doesn't prevent subsequent detection
 * - Drogue triggers immediately (already below deploy altitude)
 * - Main triggers soon after drogue (only 500m between altitudes)
 */

